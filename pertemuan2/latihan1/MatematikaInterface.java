package pertemuan2.latihan1;

// definisi sebuah interface bernama MatematikaInterface
public interface MatematikaInterface {

     // metode abstrak untuk melakukan operasi penjumlahan
     public abstract int pertambahan (int a, int b);

     // metode abstrak untuk melakukan operasi penngurangan
     public abstract int pengurangan (int a, int b);

     // metode abstrak untuk melakukan operasi perkalian
     public abstract int perkalian (int a, int b);

     // metode abstrak untuk melakukan operasi pembagian
     public abstract int pembagian (int a, int b);
}
