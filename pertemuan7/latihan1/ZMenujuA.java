package pertemuan7.latihan1;

public class ZMenujuA {
    public static void main(String[] args) {
        // mulai dari karakter 'Z' dan berakhir di karakter 'A'
        for (char huruf = 'Z'; huruf >= 'A'; huruf--) {
            System.out.print(huruf + "  "); // menampilkan huruf
        }
    }
}
